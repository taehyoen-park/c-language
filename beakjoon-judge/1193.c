#include<stdio.h>

/*
1/1	1/2	1/3	1/4	1/5	…
2/1	2/2	2/3	2/4	…	…
3/1	3/2	3/3	…	…	…
4/1	4/2	…	…	…	…
5/1	…	…	…	…	…
…	…	…	…	…	…
 1/1 -> 1/2 -> 2/1 -> 3/1 -> 2/2 -> … 과 같은 지그재그 순서로 차례대로 
1번, 2번, 3번, 4번, 5번, …분수라고 하자 
n번째는 무엇인가? 
*/

int main()
{
	int n2,num = 1,n;
	
	scanf("%d",&n);
	
	while((num * (num + 1)) / 2 < n)
	{
		num++;
	}
		
	
	n2 = n - ((num-1) * ((num-1) + 1)) / 2;
	
	if(num % 2 == 0) 
		printf("%d/%d",n2,num-(n2-1));
	else
		printf("%d/%d",num-n2+1,n2);
		
	return 0;
}

